CREATE TABLE Country (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255) UNIQUE
);


CREATE TABLE Team (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    team_api_id NUMBER UNIQUE,
    team_fifa_api_id NUMBER NOT NULL PRIMARY KEY,  
    team_long_name VARCHAR2(255),
    team_short_name VARCHAR2(50)
);


CREATE TABLE Player (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    player_api_id NUMBER UNIQUE,
    player_name VARCHAR2(255),
    player_fifa_api_id NUMBER UNIQUE,
    birthday DATE,
    height NUMBER,
    weight NUMBER
);

CREATE TABLE League (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    country_id NUMBER,
    name VARCHAR2(255) UNIQUE,
    FOREIGN KEY (country_id) REFERENCES Country(id)
);

